#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	int	i;

	i = 0;
	while (s && s[i])
	{
		write(1, &s[i], 1);
		(*len)++;
		i++;
	}
	if (!s)
	{
		write(1, "(null)", 6);
		(*len) += 6;
	}
}
